    private void setupTableMouseListener() {
        dataTable.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int selectedRow = dataTable.getSelectedRow();
                txtNombre.setText((String) dataTable.getValueAt(selectedRow, 1));
                txtApellidos.setText((String) dataTable.getValueAt(selectedRow, 2));
                txtTelefono.setText((String) dataTable.getValueAt(selectedRow,3));
                txtEmail.setText((String) dataTable.getValueAt(selectedRow,4));
                txtRFC.setText((String) dataTable.getValueAt(selectedRow,5));
                }
            });
        }
    
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String name = txtNombre.getText();
        String lastnames = txtApellidos.getText();
        String phone = txtTelefono.getText();
        String email = txtEmail.getText();
        String rfc = txtRFC.getText();
         
        if(Utilidades.validarCorreo(email)){
            Customer cliente = new Customer(name, lastnames, phone, email, rfc);
            controller.createCustomer(cliente);
            cargarDatos();
        } else {
            lblAlertaEmail.setVisible(true);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
           int selectedRow = dataTable.getSelectedRow();
        if (selectedRow >= 0) {
            int idCustomer = (int) dataTable.getValueAt(selectedRow, 0);
            String name = txtNombre.getText();
            String lastnames = txtApellidos.getText();
            String phone = txtTelefono.getText();
            String email = txtEmail.getText();
            String rfc = txtRFC.getText();

            Customer cliente = new Customer(idCustomer, name,lastnames, phone, email, rfc);
            controller.updateCustomer(cliente);
            cargarDatos();
        } else {
            // Mostrar mensaje de error
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una fila para editar.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

