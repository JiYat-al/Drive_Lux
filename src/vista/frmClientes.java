        private void cargarDatos() {
            List<Customer> clientes = controller.getAllCustomers();
            DefaultTableModel model = (DefaultTableModel) dataTable.getModel();
            model.setRowCount(0);
            for (Customer cliente : clientes) {
                model.addRow(new Object[]{cliente.getIdCustomer(),cliente.getName(),cliente.getLastName(),cliente.getPhone(),cliente.getEmail(),cliente.getRFC()});
            }
    }

